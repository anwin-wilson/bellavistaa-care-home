// Function to get URL parameter
function getUrlParameter(name) {
    const urlParams = new URLSearchParams(window.location.search);
    return urlParams.get(name);
}

// Function to load news content dynamically
function loadNewsContent() {
    const newsId = getUrlParameter('id');
    
    if (!newsId || !newsData[newsId]) {
        // Default to COVID update if no ID or invalid ID
        loadDefaultNews();
        return;
    }
    
    const news = newsData[newsId];
    
    // Update page title
    document.title = `${news.title} - Bellavista News`;
    
    // Update main story content
    updateMainStory(news);
}

function loadDefaultNews() {
    const news = newsData['covid-update'];
    updateMainStory(news);
}

function updateMainStory(news) {
    // Update image
    const mainImage = document.querySelector('.main-story-image img');
    if (mainImage) {
        mainImage.src = news.image;
        mainImage.alt = news.title;
    }
    
    // Update badge
    const badge = document.querySelector('.story-badge');
    if (badge) {
        badge.textContent = news.badge;
    }
    
    // Update meta information
    const metaElements = document.querySelectorAll('.story-meta span');
    if (metaElements.length >= 3) {
        metaElements[0].innerHTML = `<i class="fas fa-calendar"></i> ${news.date}`;
        metaElements[1].innerHTML = `<i class="fas fa-map-marker-alt"></i> ${news.location}`;
        metaElements[2].innerHTML = `<i class="fas fa-user"></i> ${news.author}`;
    }
    
    // Update title
    const title = document.querySelector('.story-title');
    if (title) {
        title.textContent = news.title;
    }
    
    // Update excerpt
    const excerpt = document.querySelector('.story-excerpt');
    if (excerpt) {
        excerpt.textContent = news.excerpt;
    }
    
    // Update main content
    const content = document.querySelector('.story-content');
    if (content) {
        content.innerHTML = news.content;
    }
    
    // Update engagement stats
    const viewsElement = document.querySelector('.engagement-stats span:first-child');
    if (viewsElement) {
        viewsElement.innerHTML = `<i class="fas fa-eye"></i> ${news.views.toLocaleString()} views`;
    }
    
    const likeCount = document.querySelector('.like-count');
    if (likeCount) {
        likeCount.textContent = news.likes;
    }
    
    const shareCount = document.querySelector('.share-count');
    if (shareCount) {
        shareCount.textContent = news.shares;
    }
    
    const commentCount = document.querySelector('.engagement-btn:last-child');
    if (commentCount) {
        commentCount.innerHTML = `<i class="fas fa-comment"></i> ${news.comments}`;
    }
}

// Initialize when page loads
document.addEventListener('DOMContentLoaded', function() {
    loadNewsContent();
});